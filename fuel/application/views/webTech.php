		<h1>Website Framework</h1>
		<p><a href="https://codeigniter.com">CodeIgniter MVC Framework</a> is used for the overall website.  MVC stands for Model View Controller.&nbsp&nbspA controller file controls posting data back and forth between the HTML pages
		, which are the Views of MVC.&nbsp&nbspModel files control the interface with MySQL databases through CodeIgniter functions.  This includes an Active Record object-oriented database interface.  
		In certain applications on the site, such as the Orominer Human Biology (<a href="orominer1">Orominer1</a>, <a href="orominer2">Orominer2</a>) and <a href="amino">Mini-Motif</a> (Amino Acid Sequencing)
	    applications, PHP is used to directly relate to MySQL to build databases and query them for data.
		</p>
		<p>Other MVC Frameworks are coded into this site including <a href="https://laravel.com">Laravel MVC</a>, <a href="https://www.wordpress.com">WordPress</a>, and the <a href="https://www.getfuelcms.com">Fuel-CMS</a>(CodeIgniter-based) Content Management System. I have transitioned to FUEL CMS (Content Management System based
		on CodeIgniter.  Modular code style is implemented with code storage in a database.
		</p>

		<h1>Integrated Technologies</h1>
		<p><a href="https://jquery.com">Jquery</a>, <a href="https://jqueryui.com">Jquery UI</a>, <a href="https://jquerymobile.com">JQuery Mobile</a>, JavaScript, HTML, CSS, CSS3 are integrated into front-end coding.  Embedded-PHP code is used to place posted-data into variables for the JavaScript and HTML code as needed for 
		MVC-View web pages.  AJAX is used to send data to PHP-based-MySQL methods, run on the Apache server, for building databases or executing SQL queries.  AJAX is also used to store and retreive data from a MySQL database which includes
		html code for game board states.
		</p> 
		<p>Other technologies used throughout the website are Document Object Model (DOM), Scalable Vector Graphics (<a href="https://www.w3.org/Graphics/SVG/">SVG</a>), Extensible Markup Language (XML), Regular Expression (Regex), Cascading Style Sheets (CSS),
		CSS3 with 2-dimensional, and 3-dimensional Transformations.
		</p>
		<p>Also I have applications that utilize Angular2 and Redux technology for centralized management of immutable application state.
		</p>

